package class09;

public class ForLoop {

	public static void main(String[] args) {
		//  FOR LOOP
		
		
		for (int i=1; i<=5;i++) { //outer for loop
			
			for (int j=1; j<=5;j++) { //inner for loop
		System.out.print(j);
		} //End of inner for loop
			System.out.println();
	}  //End of outer for loop

	}
	}


