package class01;

public class AritmaticOperator {

	public static void main(String[] args) {
		// OPERATOR  + - * / %
		
		int num1 = 10, num2 = 20, result;
		double decimalNum1 = 9.25, decimalNum2 = 5.75, decimalResult;
		
		result = num1 + num2;
		System.out.println(result);
		
		result = num1 - num2;
		System.out.println(result);
		
		result = num1 * num2;
		System.out.println(result);
		
		result = num2 / num1;
		System.out.println(result);
		
		decimalResult = decimalNum1 + decimalNum2;
		System.out.println(decimalResult);
	}

}
