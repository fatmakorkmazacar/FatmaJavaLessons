package class01;

public class DataTypes {

	public static void main(String[] args) {
		// Data Types
		
		int num1 = 45; // numbers
		double numWithDecimal = 45.76; // decimal numbers
		char firstCharacter = 'c'; // single character
		String line1 = "This is my first java class"; // words
		boolean result = false; // true or false
		
		System.out.println(num1);
		System.out.println(numWithDecimal);
		System.out.println(firstCharacter);
		System.out.println(line1);
		System.out.println(result);
		
	}

}
